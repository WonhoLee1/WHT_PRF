    plt.subplot(1, 2, 2)
    if len(ref_times) > 0:
        plt.plot(ref_times, ref_strains, 'k--', label='Abaqus')
    plt.plot(times, strains, '-s', label='JAX PRF', alpha=0.7)
    plt.xlabel('Time')
    plt.ylabel('Strain (11)')
    plt.title(f"Strain vs Time (R2: {{metrics['strain_r2']:.4f}})" if metrics['strain_r2'] is not None else "Strain vs Time")
    plt.legend()
    
    plt.tight_layout()
    png_name = "{os.path.basename(inp_filepath).replace('.inp', '.png')}"
    plt.savefig(png_name)
    print(f"Saved plot to {{png_name}}")

